CMaterial::CMaterial()
{
}

//=================================================================================================
//　　　マテリアルクラスデストラクタ                                     
//=================================================================================================
CMaterial::~CMaterial()
{
}

//=================================================================================================
//　　　マテリアルクラス更新処理                                    
//=================================================================================================
void CMaterial::Update(void)
{
	LPDIRECT3DDEVICE9 pDevice = CRenderer::GetD3DDevice();

	//マテリアル設定
	D3DMATERIAL9 mat;
	ZeroMemory(&mat, sizeof(mat));
	mat.Diffuse.r = 1.0f;
	mat.Diffuse.g = 1.0f;
	mat.Diffuse.b = 1.0f;
	mat.Diffuse.a = 1.0f;
	//マテリアルに追加
	mat.Ambient.r = 0.9f;
	mat.Ambient.g = 0.0f;
	mat.Ambient.b = 0.0f;
	mat.Ambient.a = 1.0f;
	pDevice->SetMaterial(&mat);
}
